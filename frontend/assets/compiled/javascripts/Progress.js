(function() {

"use strict";

angular.module("BitriseAddonFirebaseApp").factory("Progress", function() {

	var Progress = function() {
		this.message;
		this.isIdle;
		this.isInProgress;
		this.isSuccess;
		this.isError;
		this.cssClass;

		this.reset();
	};

	Progress.prototype.start = function(message) {
		this.message = message;
		this.isIdle = false;
		this.isInProgress = true;
		this.isSuccess = false;
		this.isError = false;
		this.cssClass = "in-progress";
	};

	Progress.prototype.error = function(error) {
		this.message = error.message;
		this.isIdle = false;
		this.isInProgress = false;
		this.isSuccess = false;
		this.isError = true;
		this.cssClass = "error";
	};

	Progress.prototype.success = function() {
		this.reset();
		this.isSuccess = true;
	};

	Progress.prototype.reset = function() {
		this.message = null;
		this.isIdle = true;
		this.isInProgress = false;
		this.isSuccess = false;
		this.isError = false;
		this.cssClass = null;
	};

	return Progress;

});

})();
